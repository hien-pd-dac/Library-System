    /**
     * Function chèn dữ liệu ngày, tháng, năm vào trong combobox
     */
    private void setExperiedDay(){
        int i;
        for(i = 1; i <= 31; i++){
            cbbDay.addItem(i);
        }
        for(i = 1; i <= 12; i++){
            cbbMonth.addItem(i);
        }
        for(i = 1900; i <= 2100; i++){
            cbbYear.addItem(i);
        }
    }
